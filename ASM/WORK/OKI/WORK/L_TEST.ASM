INCLUDE	MDOS.H
INCLUDE	STD.H
;INCLUDE	UTY.H

CODE	SEGMENT	PUBLIC
	ASSUME	CS:CODE,DS:DATA

	EXTRN	LOCATE:NEAR

START:		MOV	AX,DATA
		MOV	DS,AX

		DISPLAY_CHAR	CLS

		MOV	AH,1
		MOV	AL,1

LOOP:		ADD	AH,1
		CMP	AH,24
		JA	NEXT
		PUSH	AX
		CALL	LOCATE
		DISPLAY	MSG
		READ_KBD
		POP	AX
		JMP	LOOP

NEXT:		DISPLAY_CHAR	CLS
		MOV	AH,1
		MOV	AL,1

LOOP1:		ADD	AL,1
		CMP	AL,79
		JA	END
		PUSH	AX
		CALL	LOCATE
		DISPLAY	MSG
		READ_KBD
		POP	AX
		JMP	LOOP1


END:		END_PROCESS

CODE		ENDS

DATA	SEGMENT	PUBLIC

MSG	DB	'ＬＯＣＡＴＥ　サブルーチン　テスト',CR,LF,'$'

DATA	ENDS
	END	START

